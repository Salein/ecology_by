_BY_REGION_HINTS_SORTED: tuple[tuple[str, tuple[float, float]], ...] = tuple(
    sorted(_BY_REGION_HINTS, key=lambda x: len(x[0]), reverse=True)
)


def _normalize_for_match(text: str) -> str:
    t = (text or "").replace("\xa0", " ").casefold()
    t = re.sub(r"[^\w\sа-яёіўґ']", " ", t, flags=re.IGNORECASE)
    t = re.sub(r"\s+", " ", t).strip()
    return f" {t} "


def approx_coords_from_by_text(address: str, object_name: str = "") -> tuple[float, float] | None:
    """Если в тексте есть известный НП — возвращает приблизительный центр."""
    blob = _normalize_for_match(f"{address} {object_name}")
    if len(blob) < 6:
        return None
    # Сначала длинные фразы (область), затем совпадение по словам — O(слов) вместо перебора ~50k имён
    for name, (la, lo) in _BY_REGION_HINTS_SORTED:
        if f" {name.casefold()} " in blob:
            return la, lo
    merged = _load_merged_centers()
    words = {w for w in blob.split() if len(w) >= 4}
    for w in sorted(words, key=len, reverse=True):
        hit = merged.get(w)
        if hit is not None:
            return hit[0], hit[1]
    return None


def approx_coords_from_locality_in_address(address: str) -> tuple[float, float] | None:
    """
    Более строгий ориентир: извлекает населённый пункт по маркерам из адреса (г./аг./д./пос.).
    Нужен для sanity-check, чтобы не цепляться за двусмысленные слова улиц.
    """
    if not address:
        return None
    merged = _load_merged_centers()
    text = address.replace("\xa0", " ")
    markers = re.findall(
        r"\b(?:г\.|г/п|аг\.|д\.|дер\.|п\.|пос\.|поселок|городок)\s*([^,;\n]{1,80})",
        text,
        flags=re.IGNORECASE,
    )
    stop_words = {
        "район",
        "районе",
        "область",
        "области",
        "обл",
        "обл.",
        "вобласць",
        "получения",
        "использования",
        "использованию",
        "использует",
        "принимает",
        "отходов",
        "вторичного",
        "сырья",
        "ул",
        "улица",
        "проспект",
        "пр",
        "пер",
        "д",
        "дом",
    }
    for m in markers:
        tail = re.sub(r"\s+", " ", (m or "").strip())
        if not tail:
            continue
        tail = re.sub(r"^[\"'«»()\[\]\-–—\s]+|[\"'«»()\[\]\-–—\s]+$", "", tail)
        parts = [p for p in tail.split(" ") if p]
        if not parts:
            continue
        trimmed: list[str] = []
        for p in parts:
            p_norm = re.sub(r"[^\wа-яёіўґ-]", "", p, flags=re.IGNORECASE).casefold()
            if not p_norm:
                continue
            if p_norm in stop_words:
                break
            trimmed.append(p_norm)
            if len(trimmed) >= 3:
                break
        if not trimmed:
            continue
        candidates: list[str] = []
        joined = " ".join(trimmed)
        if joined:
            candidates.append(joined)
        for i in range(min(3, len(trimmed)), 0, -1):
            cand = " ".join(trimmed[:i]).strip()
            if cand and cand not in candidates:
                candidates.append(cand)
        for cand in candidates:
            hit = merged.get(cand)
            if hit is not None:
                return hit[0], hit[1]
    return None
