_GEONAMES_JSON = Path(__file__).resolve().parent.parent / "data" / "by_geonames_centroids.json"
_MERGED_CENTERS: dict[str, tuple[float, float]] | None = None


def _load_merged_centers() -> dict[str, tuple[float, float]]:
    global _MERGED_CENTERS
    if _MERGED_CENTERS is not None:
        return _MERGED_CENTERS
    geo: dict[str, tuple[float, float]] = {}
    if _GEONAMES_JSON.is_file():
        raw = json.loads(_GEONAMES_JSON.read_text(encoding="utf-8"))
        for k, pair in raw.items():
            if isinstance(pair, list) and len(pair) >= 2:
                try:
                    geo[str(k).casefold()] = (float(pair[0]), float(pair[1]))
                except (TypeError, ValueError):
                    pass
    # ручной словарь перекрывает GeoNames при совпадении ключа
    _MERGED_CENTERS = {**geo, **{k.casefold(): v for k, v in _BY_CENTERS.items()}}
    return _MERGED_CENTERS

