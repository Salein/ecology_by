export function formatWasteTypeDisplay(wasteTypeName: string | null | undefined): string {
  let value = (wasteTypeName || "").replace(/\u00a0/g, " ").replace(/\s+/g, " ").trim();
  if (!value) return "—";

  const cutMarkers = [
    /\b(?:Объект|Собственник)\b/i,
    /(^|[^A-Za-zА-Яа-яЁё])(?:ООО|ОАО|ЗАО|УП|РУП|ЧУП|ОДО|ИП)(?=$|[^A-Za-zА-Яа-яЁё])/i,
    /\b(?:Общество\s+с\s+(?:ограниченной|дополнительной)\s+ответственностью|Открытое\s+акционерное\s+общество|Закрытое\s+акционерное\s+общество|Частное\s+унитарное\s+предприятие)\b/i,
    /\b\d{6}\b/,
    /(?:\+?\s*375|8-0?\d{2,4}|\(\s*0\d{2,4}\s*\)|(?:^|[^A-Za-zА-Яа-яЁё])тел\.?(?=$|[^A-Za-zА-Яа-яЁё])|(?:^|[^A-Za-zА-Яа-яЁё])факс(?=$|[^A-Za-zА-Яа-яЁё]))/i,
    /\(/,
    /(?:^|[^A-Za-zА-Яа-яЁё])(?:реконструкц(?:ия|ии)|техническ(?:ая|ий|ое|ие)\s+модернизац(?:ия|ии)|строительств(?:о|а)|возведение|площадка\s+для\s+приема|площадка\s+по\s+подготовке|полигон\s+тбо)(?=$|[^A-Za-zА-Яа-яЁё])/i,
    /\bв\s+соответствии\s+с\s+законодательством\b/i,
    /\bоб\s+охране\s+окружающей\s+среды\b/i,
    /(?:биогазов(?:ый|ая|ое|ые)|котельн(?:ая|ый|ое|ые)|машина\s+рубильная|измельчител(?:ь|я)|дробильно-сортировочн(?:ый|ая|ое|ые)|дробильн(?:ый|ая|ое|ые)|стационарн(?:ый|ая|ое|ые)|мобильн(?:ый|ая|ое|ые)|агломератор|гранулятор|шредер|комплекс|установка|цех|участок|площадк[аи]|полигон|пила|экскаватор|гидромолот)/i,
  ];
  const cuts = cutMarkers.map((re) => value.search(re)).filter((idx) => idx >= 0);
  if (cuts.length > 0) value = value.slice(0, Math.min(...cuts));

  value = value.replace(/^[*•"'«»\s\-–—]+/, "").replace(/[;,.:\-–—\s]+$/g, "").trim();
  return value || "—";
}
